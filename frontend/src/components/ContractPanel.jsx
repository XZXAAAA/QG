// ContractPanel
// ---------------------------------------------------------------------------
const ContractPanel = () => {
  const [file, setFile] = useState(null);
  const [fileChars, setFileChars] = useState(null);
  const [analysis, setAnalysis] = useState(null);
  const [status, setStatus] = useState('idle'); // idle|uploading|analyzing|success|error
  const [error, setError] = useState('');
  const [dragOver, setDragOver] = useState(false);
  const fileInputRef = useRef(null);
  const isMountedRef = useRef(true);

  useEffect(() => {
    isMountedRef.current = true;
    try {
      const raw = window.localStorage.getItem(CONTRACT_PANEL_CACHE_KEY);
      if (!raw) return;
      const cached = JSON.parse(raw);
      if (cached.fileName) {
        setFile({ name: cached.fileName });
      }
      setFileChars(cached.fileChars ?? null);
      setAnalysis(cached.analysis ?? null);
      setStatus(cached.status || 'idle');
      setError(cached.error || '');
    } catch {
      // ignore cache parse errors
    }
    return () => {
      isMountedRef.current = false;
    };
  }, []);

  useEffect(() => {
    const payload = {
      fileName: file?.name || '',
      fileChars,
      analysis,
      status,
      error,
    };
    try {
      window.localStorage.setItem(CONTRACT_PANEL_CACHE_KEY, JSON.stringify(payload));
    } catch {
      // ignore storage write errors
    }
  }, [analysis, error, file?.name, fileChars, status]);

  // ── File selection ────────────────────────────────────────────────────────
  const selectFile = useCallback((selected) => {
    if (!selected) return;
    setFile(selected);
    setAnalysis(null);
    setFileChars(null);
    setError('');
    setStatus('idle');
  }, []);

  const handleInputChange = (e) => selectFile(e.target.files?.[0]);

  const handleDragOver = (e) => { e.preventDefault(); setDragOver(true); };
  const handleDragLeave = ()  => setDragOver(false);
  const handleDrop = (e) => {
    e.preventDefault();
    setDragOver(false);
    selectFile(e.dataTransfer.files?.[0]);
  };

  // ── Upload + Analyze ──────────────────────────────────────────────────────
  const handleAnalyze = async () => {
    if (!file) return;
    setStatus('uploading');
    setError('');
    setAnalysis(null);

    try {
      // Step 1 — upload raw bytes, pass filename as query param
      const uploadRes = await fetch(
        `${API_BASE_URL}/contract/upload?filename=${encodeURIComponent(file.name)}`,
        { method: 'POST', body: file }
      );
      const uploadData = await uploadRes.json().catch(() => ({}));
      if (!uploadRes.ok) {
        throw new Error(uploadData.error || `Upload failed (HTTP ${uploadRes.status})`);
      }
      const { file_id, chars } = uploadData;
      if (!file_id) throw new Error('Server did not return a file_id.');
      if (isMountedRef.current) {
        setFileChars(chars);
      }

      // Step 2 — run analysis
      setStatus('analyzing');
      const analyzeRes = await fetch(`${API_BASE_URL}/contract/analyze`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ file_id }),
      });
      const analyzeData = await analyzeRes.json().catch(() => ({}));
      if (!analyzeRes.ok) {
        throw new Error(analyzeData.error || `Analysis failed (HTTP ${analyzeRes.status})`);
      }

      if (isMountedRef.current) {
        setAnalysis(analyzeData);
        setStatus('success');
      }
    } catch (err) {
      if (isMountedRef.current) {
        setError(err.message || 'Unknown error occurred.');
        setStatus('error');
      }
    }
  };

  // ── Helpers ───────────────────────────────────────────────────────────────
  const isWorking = status === 'uploading' || status === 'analyzing';

  const buttonLabel = {
    idle:      'Analyze Contract',
    uploading: 'Uploading…',
    analyzing: 'Analyzing with AI…',
    success:   '✅ Re-analyze',
    error:     'Retry',
  }[status] || 'Analyze Contract';

  // ── Render ────────────────────────────────────────────────────────────────
  return (
    <section className="panel">

      {/* Hero */}
      <div className="panel-hero">
        <div>
          <div className="panel-kicker">AI Contract Review</div>
          <h1>Contract Review</h1>
          <p>
            Upload a PDF or DOCX contract for a clause-by-clause risk analysis
            with Chinese law perspective and AI-generated opinions.
          </p>
        </div>
      </div>

      {/* Drop zone */}
      <div
        className={`upload-area${dragOver ? ' drag-over' : ''}`}
        onClick={() => fileInputRef.current?.click()}
        onDragOver={handleDragOver}
        onDragLeave={handleDragLeave}
        onDrop={handleDrop}
      >
        <div className="upload-icon">📄</div>
        <p className="upload-text">
          {file ? `📎 ${file.name}` : 'Click or drag & drop to upload a contract'}
        </p>
        <p className="upload-hint">Supports PDF and DOCX · Max 10 MB</p>
        <input
          ref={fileInputRef}
          type="file"
          accept=".pdf,.docx"
          onChange={handleInputChange}
          style={{ display: 'none' }}
        />
      </div>

      {/* Error banner */}
      {error && (
        <div className="info-banner danger" style={{ marginBottom: 16 }}>
          <strong>Error:</strong> {error}
        </div>
      )}

      {/* Analyze + Export buttons */}
      {file && (
        <div className="contract-action-row">
          <button
            className="primary-button"
            onClick={handleAnalyze}
            disabled={isWorking}
          >
            {buttonLabel}
          </button>
          {analysis && (
            <button
              className="secondary-button"
              onClick={() => {
                const reportName = file.name.replace(/\.[^.]+$/, '') + '_review_report.md';
                downloadText(buildMarkdownReport(file.name, analysis), reportName);
              }}
            >
              ⬇ Export Report
            </button>
          )}
          {fileChars && (
            <span className="contract-chars-note">
              {fileChars.toLocaleString()} chars extracted
            </span>
          )}
        </div>
      )}

      {/* Analysis results */}
      {analysis && <AnalysisResults analysis={analysis} />}

    </section>
  );
};

