const RISK_META = {
  LOW:      { cls: 'risk-low',      label: 'LOW',      hex: '#77d6c3' },
  MEDIUM:   { cls: 'risk-medium',   label: 'MEDIUM',   hex: '#f0bf68' },
  HIGH:     { cls: 'risk-high',     label: 'HIGH',     hex: '#ff8d7d' },
  CRITICAL: { cls: 'risk-critical', label: 'CRITICAL', hex: '#ff5555' },
};

function riskMeta(level) {
  return RISK_META[level] || RISK_META.MEDIUM;
}

// ---------------------------------------------------------------------------
// RiskBadge
// ---------------------------------------------------------------------------
function RiskBadge({ level, small = false }) {
  const meta = riskMeta(level);
  return (
    <span
      style={{
        display: 'inline-block',
        padding: small ? '3px 8px' : '5px 11px',
        borderRadius: '999px',
        border: `1px solid ${meta.hex}55`,
        background: `${meta.hex}18`,
        color: meta.hex,
        fontWeight: 700,
        fontSize: small ? '0.75rem' : '0.82rem',
        letterSpacing: '0.08em',
        textTransform: 'uppercase',
        whiteSpace: 'nowrap',
      }}
    >
      {meta.label}
    </span>
  );
}

