import React, { useCallback, useEffect, useRef, useState } from 'react';
import ReactMarkdown from 'react-markdown';
import remarkGfm from 'remark-gfm';

const API_BASE_URL =
  import.meta.env.VITE_API_BASE_URL || 'http://127.0.0.1:5000/api';

const CONTRACT_PANEL_CACHE_KEY = 'qg-contract-panel-cache-v1';

// ---------------------------------------------------------------------------
// Report export helpers
// ---------------------------------------------------------------------------
function buildMarkdownReport(filename, analysis) {
  const {
    riskScore = 0, riskLevel = '', summary = '',
    parties = [], governingLaw = '', jurisdiction = '',
    clauseAnalysis = [], missingClauses = [],
    criticalClauses = [], suggestions = [], faruiOpinion = null,
  } = analysis;

  const now = new Date().toLocaleString();
  const lines = [
    `# Contract Review Report`,
    ``,
    `**File:** ${filename}  `,
    `**Generated:** ${now}  `,
    `**Overall Risk:** ${riskLevel} (${riskScore}/100)`,
    ``,
    `---`,
    ``,
    `## Executive Summary`,
    ``,
    summary,
    ``,
    `---`,
    ``,
    `## Parties`,
    ``,
    ...parties.map((p) => `- **${p.role || 'Party'}** — ${p.name}${p.jurisdiction ? ` *(${p.jurisdiction})*` : ''}`),
    ``,
    `**Governing Law:** ${governingLaw}  `,
    `**Jurisdiction:** ${jurisdiction}`,
    ``,
    `---`,
    ``,
    `## Clause Analysis`,
    ``,
    `| Clause | Risk | Issue | Suggestion |`,
    `|--------|------|-------|------------|`,
    ...clauseAnalysis.map((c) =>
      `| ${c.clause} | **${c.risk}** | ${c.issue.replace(/\|/g, '\\|')} | ${c.suggestion.replace(/\|/g, '\\|')} |`
    ),
    ``,
    `---`,
    ``,
    `## Missing Clauses`,
    ``,
    missingClauses.length ? missingClauses.map((c) => `- ${c}`).join('\n') : '_None identified._',
    ``,
    `## Critical Clauses`,
    ``,
    criticalClauses.length ? criticalClauses.map((c) => `- ${c}`).join('\n') : '_None identified._',
    ``,
    `---`,
    ``,
    `## Recommendations`,
    ``,
    ...suggestions.map((s) => `**[${s.type}]** ${s.text}`),
  ];

  if (faruiOpinion) {
    lines.push(``, `---`, ``, `## AI Legal Opinion`, ``, faruiOpinion);
  }

  lines.push(``, `---`, ``, `*This report is generated by AI for reference only and does not constitute formal legal advice.*`);
  return lines.join('\n');
}

function downloadText(content, filename) {
  const blob = new Blob([content], { type: 'text/markdown;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  a.remove();
  URL.revokeObjectURL(url);
}

